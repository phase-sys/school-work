# B. Create a program displaying the below output:

# Enter value for x : 10
x = int(input("Enter value for x: "))

# Enter value for y : 5
y = int(input("Enter value for y: "))

# x + y = 15
print("x+y = ", x + y)

# x-y = 5
print("x-y = ", x - y)

# x*y = 50
print("x*y = ", x * y)

# x/y = 2
print("x/y = ", int(x / y))

# x>y = TRUE
print("x>y = ", str(x > y).upper())

# x<y = FALSE
print("x<y = ", str(x < y).upper())